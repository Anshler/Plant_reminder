import random

def GetRGBA():
    r = random.random()  # Random float between 0 and 1
    g = random.random()  # Random float between 0 and 1
    b = random.random()  # Random float between 0 and 1
    return (r, g, b, 1)
def GetRGB():
    r = random.random()  # Random float between 0 and 1
    g = random.random()  # Random float between 0 and 1
    b = random.random()  # Random float between 0 and 1
    return (r, g, b)
